    private void jm_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jm_buscarActionPerformed
        // TODO add your handling code here:
        String buscar = JOptionPane.showInputDialog("Buscar:");

// Obtener el texto actual del JTextField
String texto = txt_texto.getText();

// Verificar que la entrada no sea nula y que el texto contenga la palabra buscada
if (buscar != null && texto.contains(buscar)) {
    // Encontrar la posición de la palabra buscada en el texto
    int index = texto.indexOf(buscar);
    
    // Seleccionar la palabra encontrada en el JTextField
    txt_texto.select(index, index + buscar.length());
} else {
    // Mostrar un mensaje de error si el texto no se encuentra
    JOptionPane.showMessageDialog(null, "Texto no encontrado");
        }
    }//GEN-LAST:event_jm_buscarActionPerformed

    private void jm_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jm_guardarActionPerformed
        // TODO add your handling code here:
        // Verifica si ya se ha seleccionado un archivo
    if (archivo != null) {
    // Obtener el texto del JTextArea
    String documento = this.txt_texto.getText();
    
    // Llamar a la función para guardar el documento en el archivo
    String mensaje = GuardarArchivo(archivo, documento);
    
    // Verificar si se recibió un mensaje de error
    if (mensaje != null) {
        // Mostrar mensaje de error o advertencia
        JOptionPane.showMessageDialog(null, mensaje);
    } else {
        // Mostrar mensaje de éxito
        JOptionPane.showMessageDialog(null, "Archivo guardado correctamente.");
    }
} else {
    // Mostrar mensaje si no se ha seleccionado un archivo
    JOptionPane.showMessageDialog(null, "Primero debe usar 'Guardar como' para seleccionar un archivo.");
    }
    }//GEN-LAST:event_jm_guardarActionPerformed

