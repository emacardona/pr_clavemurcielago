    private void btn_procesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_procesarActionPerformed
        // TODO add your handling code here:
        // Obtener el texto de entrada
    String cadena = this.txt_texto.getText();

    // Calcular y mostrar la longitud de la cadena
    int longitudCadena = cadena.length();
    txt_longitud.setText(String.valueOf(longitudCadena));

    // Dividir la cadena en palabras y contar
    String[] palabras = cadena.split("\\s+");
    int cantidadPalabras = palabras.length;
    txt_totalp.setText(String.valueOf(cantidadPalabras));

    // Obtener la primera, última y letra central de la cadena
    char primeraLetra = cadena.charAt(0);
    char ultimaLetra = cadena.charAt(longitudCadena - 1);
    char letraCentral;
    int indiceCentralL = longitudCadena / 2;

    if (longitudCadena % 2 == 0) {
    letraCentral = cadena.charAt(indiceCentralL - 1);
    } else {
    letraCentral = cadena.charAt(indiceCentralL);
}

    txt_primeral.setText(String.valueOf(primeraLetra));
    txt_letrac.setText(String.valueOf(ultimaLetra));
    txt_ultimal.setText(String.valueOf(letraCentral));

    // Obtener la primera, última y palabra central
    String primeraPalabra = palabras[0];
    String ultimaPalabra = palabras[cantidadPalabras - 1];
    String palabraCentral;
    int indiceCentralP = cantidadPalabras / 2;

    if (cantidadPalabras % 2 == 0) {
        palabraCentral = palabras[indiceCentralP - 1];
    } else {
        palabraCentral = palabras[indiceCentralP];
    }

    txt_primerap.setText(primeraPalabra);
    txt_palabrac.setText(palabraCentral);
    txt_ultimap.setText(ultimaPalabra);

    // Contar vocales
    int contadorA = 0, contadorE = 0, contadorI = 0, contadorO = 0, contadorU = 0;

    for (char c : cadena.toCharArray()) {
        switch (Character.toLowerCase(c)) {
            case 'a': case 'á': contadorA++; break;
            case 'e': case 'é': contadorE++; break;
            case 'i': case 'í': contadorI++; break;
            case 'o': case 'ó': contadorO++; break;
            case 'u': case 'ú': contadorU++; break;
            }
        }

        txt_letraa.setText(String.valueOf(contadorA));
        txt_letrae.setText(String.valueOf(contadorE));
        txt_letrai.setText(String.valueOf(contadorI));
        txt_letrao.setText(String.valueOf(contadorO));
        txt_letrau.setText(String.valueOf(contadorU));

        // Contar palabras pares e impares
        int contadorPalabrasPar = 0;
        int contadorPalabrasImpar = 0;

    for (String palabra : palabras) {
        if (palabra.length() % 2 == 0) {
            contadorPalabrasPar++;
    } else {
        contadorPalabrasImpar++;
        }
    }

    this.txt_palabrasp.setText(String.valueOf(contadorPalabrasPar));
    this.txt_palabrasi.setText(String.valueOf(contadorPalabrasImpar));

    // Convertir texto a "murciélago"
    Map<Character, String> claveMurcielago = new HashMap<>();
        claveMurcielago.put('a', "7");
        claveMurcielago.put('b', "b");
        claveMurcielago.put('c', "3");
        claveMurcielago.put('d', "d");
        claveMurcielago.put('e', "5");
        claveMurcielago.put('f', "f");
        claveMurcielago.put('g', "8");
        claveMurcielago.put('h', "h");
        claveMurcielago.put('i', "4");
        claveMurcielago.put('j', "j");
        claveMurcielago.put('k', "k");
        claveMurcielago.put('l', "6");
        claveMurcielago.put('m', "0");
        claveMurcielago.put('n', "n");
        claveMurcielago.put('o', "9");
        claveMurcielago.put('p', "p");
        claveMurcielago.put('q', "q");
        claveMurcielago.put('r', "2");
        claveMurcielago.put('s', "s");
        claveMurcielago.put('t', "1");
        claveMurcielago.put('u', "3");
        claveMurcielago.put('v', "v");
        claveMurcielago.put('w', "w");
        claveMurcielago.put('x', "x");
        claveMurcielago.put('y', "y");
        claveMurcielago.put('z', "z");

// Convertir la cadena a murciélago
StringBuilder resultado = new StringBuilder();
for (char c : cadena.toCharArray()) {
    char letra = Character.toLowerCase(c);
    if (c == ' ') {
        resultado.append(" ");
    } else {
        resultado.append(claveMurcielago.getOrDefault(letra, String.valueOf(c)));
    }
}

this.txt_murcielago.setText(resultado.toString());
        //System.out.println(resultado.toString());
    }//GEN-LAST:event_btn_procesarActionPerformed

